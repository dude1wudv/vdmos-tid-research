def relative(path: Path) -> str:
    return path.resolve().relative_to(ROOT.resolve()).as_posix()


def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for block in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(block)
    return digest.hexdigest()


def read_csv(path: Path, column_names: dict[str, str]) -> list[dict[str, float | int]]:
    rows: list[dict[str, float | int]] = []
    with path.open(encoding="utf-8-sig", newline="") as handle:
        reader = csv.DictReader(handle)
        if not reader.fieldnames:
            raise ValueError(f"CSV without header: {path}")
        for source_row, row in enumerate(reader, start=2):
            rows.append({
                "time_s": float(row[column_names["time"]]),
                "voltage_v": float(row[column_names["voltage"]]),
                "ic_a_um": float(row[column_names["current"]]),
                "tmax_k": float(row[column_names["tmax"]]),
                "source_row": source_row,
            })
    if not rows:
        raise ValueError(f"No rows in {path}")
    return rows


def phase(time_s: float) -> str:
    if time_s <= PRESTRIKE_CUTOFF_S:
        return "pre_strike"
    if time_s <= PULSE_WINDOW_S[1]:
        return "ion_pulse"
    if time_s <= 4e-10:
        return "post_pulse_decay"
    if time_s < 1e-8:
        return "early_electrical_growth"
    if time_s < 4e-8:
        return "10_to_40ns"
    return "40_to_60ns"


def derive(raw: list[dict[str, float | int]], source: Path, variant_id: str, run_id: str) -> tuple[list[dict[str, object]], float]:
    prestrike = [row for row in raw if float(row["time_s"]) <= PRESTRIKE_CUTOFF_S]
    if not prestrike:
        raise ValueError("No stable pre-strike sample at or before 90 ps")
    # The last pre-strike PLT sample is the definition; never impose a nominal 300 K reference.
    tpre_k = float(prestrike[-1]["tmax_k"])
    energy = 0.0
    rows: list[dict[str, object]] = []
    previous: dict[str, float | int] | None = None
    for raw_row in raw:
        time_s = float(raw_row["time_s"])
        voltage_v = float(raw_row["voltage_v"])
        current_a_um = float(raw_row["ic_a_um"])
        power_w_um = abs(voltage_v * current_a_um)
        if previous is not None:
            previous_power = abs(float(previous["voltage_v"]) * float(previous["ic_a_um"]))
            energy += 0.5 * (previous_power + power_w_um) * (time_s - float(previous["time_s"]))
        rows.append({
            "sample_kind": "raw_transient",
            "thermal_variant_id": variant_id,
            "run_id": run_id,
            "time_s": time_s,
            "time_ns": time_s * 1e9,
            "collector_inner_voltage_v": voltage_v,
            "ic_a_um": current_a_um,
            "tmax_k": float(raw_row["tmax_k"]),
            "tpre_k": tpre_k,
            "delta_t_k": float(raw_row["tmax_k"]) - tpre_k,
            "power_w_um": power_w_um,
            "cumulative_port_energy_j_um": energy,
            "phase": phase(time_s),
            "source_file": relative(source),
            "source_row": raw_row["source_row"],
            "diagnostic_only": True,
        })
        previous = raw_row
    return rows, tpre_k


def closest(rows: list[dict[str, object]], target_s: float) -> dict[str, object]:
    return min(rows, key=lambda row: abs(float(row["time_s"]) - target_s))


