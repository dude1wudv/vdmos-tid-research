def save_figure(figure: plt.Figure, png: Path) -> None:
    png.parent.mkdir(parents=True, exist_ok=True)
    figure.savefig(png, dpi=300, bbox_inches="tight")
    figure.savefig(png.with_suffix(".svg"), bbox_inches="tight")
    plt.close(figure)


def plot_r1_diagnostic(rows: list[dict[str, object]], output: Path) -> None:
    figure, ax_i = plt.subplots(figsize=(10.5, 6.7))
    ax_t = ax_i.twinx()
    times = [float(row["time_s"]) for row in rows]
    currents = [abs(float(row["ic_a_um"])) for row in rows]
    positive_temperature = [row for row in rows if float(row["delta_t_k"]) > 0]
    ax_i.plot(times, currents, color="#1b1b1b", marker="o", markersize=2.8, linewidth=1.45, label="Ic")
    ax_t.plot([float(row["time_s"]) for row in positive_temperature], [float(row["delta_t_k"]) for row in positive_temperature], color="#c75520", linestyle="--", marker="^", markersize=3.0, linewidth=1.35, label="ΔT")
    ax_i.set_xscale("log"); ax_i.set_yscale("log"); ax_t.set_yscale("log")
    ax_i.set_xlabel("Time (s)"); ax_i.set_ylabel("Collector current |Ic| (A/µm)"); ax_t.set_ylabel("ΔT = Tmax − Tpre (K)")
    ax_i.axvline(1e-10, color="#666666", linestyle=":", linewidth=1.0)
    ax_i.text(1e-10, min(currents) * 2, "ion strike\n100 ps", ha="center", fontsize=8.5)
    ax_i.grid(True, which="both", linestyle=":", alpha=0.35)
    ax_i.legend(handles=[Line2D([0], [0], color="#1b1b1b", marker="o", label="Ic"), Line2D([0], [0], color="#c75520", linestyle="--", marker="^", label="ΔT")], loc="upper left")
    ax_i.set_title("R1 fully thermo-coupled timing diagnostic\n2500 V; LET 15 MeV·cm²·mg⁻¹; temporal covariation only")
