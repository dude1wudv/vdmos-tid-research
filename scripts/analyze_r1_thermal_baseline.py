def validate(rows: list[dict[str, object]], keys: list[dict[str, object]], grid: list[dict[str, object]], manifest: dict[str, object]) -> None:
    if len(rows) != 158:
        raise ValueError(f"Unexpected R1 point count: {len(rows)}")
    if any(row["sample_kind"] != "raw_transient" for row in rows):
        raise ValueError("Raw R1 rows lost sample-kind provenance")
    if abs(float(rows[-1]["time_s"]) - 6e-8) > 1e-19:
        raise ValueError("R1 did not end at 60 ns")
    if abs(float(rows[17]["tpre_k"]) - 324.633215689) > 1e-9:
        raise ValueError("Unexpected last pre-strike Tmax")
    expected_keys = {
        "pre_strike_last", "pulse_peak_near_100ps", "post_pulse_sample_min", "time_0.4ns",
        "time_10ns", "time_40ns", "time_50ns", "time_60ns",
    }
    if {row["key_id"] for row in keys} != expected_keys:
        raise ValueError("Key-time set changed")
    if any(row["selection_method"] not in {"raw_exact", "raw_nearest"} for row in keys):
        raise ValueError("Invalid raw key selection method")
    expected_grid_points = sum(round((end_s - start_s) / dt_s) + 1 for _, start_s, end_s, dt_s in LINEAR_GRID_SEGMENTS)
    if len(grid) != expected_grid_points or any(row["sample_kind"] != "derived_linear_grid" for row in grid):
        raise ValueError("Derived linear grid schema/count changed")
    if manifest["lifecycle"] != "SUCCEEDED" or str(manifest["exit_code"]) != "0":
        raise ValueError("R1 manifest did not record success")


